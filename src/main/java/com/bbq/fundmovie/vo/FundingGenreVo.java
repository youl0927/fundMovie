package com.bbq.fundmovie.vo;

public class FundingGenreVo {
	
	private int funding_genre_no;
	private int funding_open_no;
	private int genre_no;
	
	public FundingGenreVo() {
		super();
	}

	public int getFunding_genre_no() {
		return funding_genre_no;
	}

	public void setFunding_genre_no(int funding_genre_no) {
		this.funding_genre_no = funding_genre_no;
	}

	public int getFunding_open_no() {
		return funding_open_no;
	}

	public void setFunding_open_no(int funding_open_no) {
		this.funding_open_no = funding_open_no;
	}

	public int getGenre_no() {
		return genre_no;
	}

	public void setGenre_no(int genre_no) {
		this.genre_no = genre_no;
	}
	
}
